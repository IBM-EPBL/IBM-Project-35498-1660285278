#creation of list
abishek = []
print(abishek)

#inserting element
abishek.insert(0,1)
print(abishek)

#removing element
abishek.remove(1)
print(abishek)

#appending element
abishek.append(1)
abishek.append(0)
abishek.append(5)
abishek.append(4)
print(abishek)

#sorting list
abishek.sort()
print(abishek)

#popping element
abishek.pop()
print(abishek)

#reversing list
abishek.reverse()
print(abishek)
