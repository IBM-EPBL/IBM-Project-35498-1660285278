#creation of list
akash = []
print(akash)

#inserting element
akash.insert(0,1)
print(akash)

#removing element
akash.remove(1)
print(akash)

#appending element
akash.append(1)
akash.append(0)
akash.append(5)
akash.append(4)
print(akash)

#sorting list
akash.sort()
print(akash)

#popping element
akash.pop()
print(akash)

#reversing list
akash.reverse()
print(akash)
