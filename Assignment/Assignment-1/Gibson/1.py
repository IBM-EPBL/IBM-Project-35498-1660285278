#creation of list
gibson = []
print(gibson)

#inserting element
gibson.insert(0,1)
print(gibson)

#removing element
gibson.remove(1)
print(gibson)

#appending element
gibson.append(1)
gibson.append(0)
gibson.append(5)
gibson.append(4)
print(gibson)

#sorting list
gibson.sort()
print(gibson)

#popping element
gibson.pop()
print(gibson)

#reversing list
gibson.reverse()
print(gibson)
