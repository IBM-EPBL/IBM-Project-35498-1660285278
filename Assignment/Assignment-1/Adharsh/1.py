#creation of list
adharsh = []
print(adharsh)

#inserting element
adharsh.insert(0,1)
print(adharsh)

#removing element
adharsh.remove(1)
print(adharsh)

#appending element
adharsh.append(1)
adharsh.append(0)
adharsh.append(5)
adharsh.append(4)
print(adharsh)

#sorting list
adharsh.sort()
print(adharsh)

#popping element
adharsh.pop()
print(adharsh)

#reversing list
adharsh.reverse()
print(adharsh)
